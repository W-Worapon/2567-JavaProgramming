import java.util.*;

public class Lab202 {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		int count;
		
		while(true){
			System.out.print("Input number : ");
			int number = input.nextInt();
			int oldnumber = number;
			
			if(number <= oldnumber) {
				count;
			}
		}
		
		System.out.println("BYE BYE");

		input.close();
	}

}
